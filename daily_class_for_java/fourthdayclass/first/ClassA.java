package daily_class_for_java.fourthdayclass.first;

public class ClassA {
    public void methodA() {
        System.out.println("Method A from Class A");
    }
    private void methodB() {
        System.out.println("Method B from Class A");
    }
    protected void methodC() {
        System.out.println("Method C from Class A");
    }
    void methodD() {
        System.out.println("Method D from Class A");
    }
    public static void methodE() {
        System.out.println("Static Method E from Class A");
    }
}
